def recommend_books(keywords):
    recommendations = []
    for keyword in keywords.split():
        keyword = keyword.lower()
        if keyword in book_recommendations:
            recommendations.extend(book_recommendations[keyword])
    
    if not recommendations:
        return "Не нашел рекомендаций по вашим ключевым словам.\nПопробуйте: программирование, психология, наука, бизнес"
    else:
        return "Рекомендуемые книги:\n- " + "\n- ".join(set(recommendations)) + \
               "\n\nХотите найти эти книги в интернете?"

